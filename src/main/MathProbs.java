package main;

public class MathProbs {
	
	public int add(int a, int b) {
		return a + b;
	}
	public int sub(int a, int b) {
		return a - b;
	}
	public int div(int a, int b) {
		return a / b;
	}
	public int mult(int a, int b) {
		return a * b;
	}
	public double circumference(double a, int d) {
		return (double) (a * d);
	}

}
